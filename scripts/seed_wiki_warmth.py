def main():
    db_path = Path(sys.argv[1]).expanduser() if len(sys.argv) > 1 \
        else Path("~/.lebotclaw/wiki.db").expanduser()
    db_path.parent.mkdir(parents=True, exist_ok=True)

    conn = sqlite3.connect(str(db_path))
    conn.executescript(_CREATE)

    inserted = 0
    for p in PAGES:
        # 幂等：先删同 title，再插
        conn.execute("DELETE FROM wiki_pages WHERE title=?", (p["title"],))
        pid = uuid.uuid4().hex[:12]
        now = time.time()
        conn.execute(
            "INSERT INTO wiki_pages (id,title,content,source,tags,created_at,updated_at) "
            "VALUES (?,?,?,?,?,?,?)",
            (pid, p["title"], p["content"], SOURCE, p["tags"], now, now),
        )
        inserted += 1
    conn.commit()

    total = conn.execute("SELECT count(*) FROM wiki_pages").fetchone()[0]
    print(f"✓ 已写入 {inserted} 页到 {db_path}")
    print(f"  当前 wiki 总页数：{total}")
    print("  页面清单：")
    for r in conn.execute("SELECT title FROM wiki_pages ORDER BY updated_at DESC"):
        print(f"    - {r[0]}")
    conn.close()


if __name__ == "__main__":
    main()
